/**
 * Enum representing the types of lessons available.
 * Used to categorize lessons based on their structure and participant type.
 */
export enum LessonType {
  /** A public lesson open to multiple participants. */
  PUBLIC = "PUBLIC",
  /** A private lesson intended for a single participant. */
  PRIVATE = "PRIVATE",
  /** A mixed lesson that combines both public and private elements. */
  MIXED = "MIXED",
}
